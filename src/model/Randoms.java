package model;

public class Randoms {
    
    private int headRandom;
    private int tailRandom;

    public Randoms(int headRandom, int tailRandom) {
        this.headRandom = headRandom;
        this.tailRandom = tailRandom;
    }

    public int getHeadRandom() {
        return headRandom;
    }

    public void setHeadRandom(int headRandom) {
        this.headRandom = headRandom;
    }

    public int getTailRandom() {
        return tailRandom;
    }

    public void setTailRandom(int tailRandom) {
        this.tailRandom = tailRandom;
    }

}
